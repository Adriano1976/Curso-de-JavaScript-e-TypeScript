// Não podemos criar variável com palavras reservadas.
// Variável precisam ter nome significativo.
// Não pode começar o nome de uma variável com um número.
// A variável não podem conter espaços ou traços.
// Utilizamos camelCase em palavras compostas em variáveis.
// Utilizamos case-sentitive em palavras em variável.
// Não podemos redeclarar variáveis com 'let'.
// Não utilize 'var', mas utilize 'let'.

let nome = 'João' // Inicializando uma variável
console.log(nome, 'nasceu em 1984.'); // Usando a variável 'nome'.
console.log('Em 2000', nome, 'conheceu Maria.');
console.log(nome, 'casou-se com Maria em 2012.');
console.log('Maria teve 1 filho com ', nome, ' em 2015');
console.log('O filho de ', nome, ' se chama Eduardo.');